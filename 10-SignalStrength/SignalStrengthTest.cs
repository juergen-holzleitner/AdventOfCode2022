    [Fact]
    public void Initial_value_of_X_is_one()
    {
      var sut = new CPU();
      var value = sut.GetRegisterX();
      value.Should().Be(1);
    }

    [Fact]
    public void Initial_cycle_number_is_zero()
    {
      var sut = new CPU();
      var value = sut.GetCycle();
      value.Should().Be(0);
    }

    [Fact]
    public void Can_process_noop()
    {
      var sut = new CPU();

      sut.Execute("noop");

      sut.GetCycle().Should().Be(1);
      sut.GetRegisterX().Should().Be(1);
    }

    [Fact]
    public void Can_process_add()
    {
      var sut = new CPU();

      sut.Execute("addx 3");

      sut.GetCycle().Should().Be(2);
      sut.GetRegisterX().Should().Be(4);
    }

    [Fact]
    public void Initial_signal_strength_is_zero()
    {
      var sut = new CPU();

      var signalStrength = sut.GetSignalStrength();
      
      signalStrength.Should().Be(0);
    }

    [Fact]
    public void Can_process_small_sample()
    {
      var input = "noop\r\naddx 3\r\naddx -5";
      var sut = new CPU();

      sut.ProcessInput(input);

      sut.GetRegisterX().Should().Be(-1);
      sut.GetCycle().Should().Be(5);
    }

